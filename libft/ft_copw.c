#include "libft.h"

char	*ft_copw(char *s2, char c2, int z2, int l)
{
	int		p2;
	char	*r;
	int		t;

	p2 = 0;
	t = 0;
	r = (char*)malloc(sizeof(char) * (l + 1));
	while ((s2[p2] != '\0') && (z2))
	{
		if ((s2[p2] != c2) && (s2[p2] != '\0'))
		{
			while ((s2[p2] != c2) && (z2 != 1))
				p2++;
			while ((s2[p2] != c2) && (z2 == 1) && (l > t))
				r[t++] = s2[p2++];
			r[t] = '\0';
			z2--;
		}
		else
			p2++;
	}
	return (r);
}
