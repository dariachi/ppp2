#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t	i;
	char	*d;
	char	*s;

	i = 0;
	d = (char *)dst;
	s = (char *)src;
	while (n)
	{
		d[i] = s[i];
		if (s[i] == c)
			return (&d[i + 1]);
		i++;
		n--;
	}
	return (NULL);
}
